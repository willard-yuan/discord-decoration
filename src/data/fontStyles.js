export const getAllFontStyles = () => {
  const allStyles = [];
  Object.entries(fontCategories).forEach(([categoryKey, category]) => {
    category.styles.forEach(style => {
      allStyles.push({
        ...style,
        category: categoryKey,
        categoryName: category.name
      });
    });
  });
  return allStyles;
};

// æ ¹æ®IDèŽ·å–å­—ä½“æ ·å¼
export const getFontStyleById = (id) => {
  const allStyles = getAllFontStyles();
  return allStyles.find(style => style.id === id);
};

// æœç´¢å­—ä½“æ ·å¼
export const searchFontStyles = (query) => {
  const allStyles = getAllFontStyles();
  const lowerQuery = query.toLowerCase();
  return allStyles.filter(style => 
    style.name.toLowerCase().includes(lowerQuery) ||
    style.categoryName.toLowerCase().includes(lowerQuery)
  );
};